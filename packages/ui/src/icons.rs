// ── 通用 SVG 宏 ────────────────────────────────────────────
macro_rules! svg_icon {
    ($name:ident, $path:literal) => {
        #[component]
        pub fn $name(
            size: Option<u32>,
            color: Option<String>,
            class: Option<String>,
        ) -> Element {
            let size = size.unwrap_or(20);
            let color = color.unwrap_or_else(|| "currentColor".to_string());
            let class = class.unwrap_or_default();
            rsx! {
                svg {
                    class: "{class}",
                    width: "{size}",
                    height: "{size}",
                    view_box: "0 0 24 24",
                    fill: "none",
                    stroke: "{color}",
                    stroke_width: "2",
                    stroke_linecap: "round",
                    stroke_linejoin: "round",
                    dangerous_inner_html: $path,
                }
            }
        }
    };
}

